package BoardVo;
public class ClinicVo {

	private int clinic_id;
	private String clinic_local;
	private String clinic_name;
	private String clinic_info;
	private String clinic_tel;
	
	
	public int getClinic_id() {
		return clinic_id;
	}
	public void setClinic_id(int clinic_id) {
		this.clinic_id = clinic_id;
	}
	public String getClinic_local() {
		return clinic_local;
	}
	public void setClinic_local(String clinic_local) {
		this.clinic_local = clinic_local;
	}
	public String getClinic_name() {
		return clinic_name;
	}
	public void setClinic_name(String clinic_name) {
		this.clinic_name = clinic_name;
	}
	public String getClinic_info() {
		return clinic_info;
	}
	public void setClinic_info(String clinic_info) {
		this.clinic_info = clinic_info;
	}
	public String getClinic_tel() {
		return clinic_tel;
	}
	public void setClinic_tel(String clinic_tel) {
		this.clinic_tel = clinic_tel;
	}
	
	
}
